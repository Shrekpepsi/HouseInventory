    private void delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delActionPerformed
        // TODO update your handling code here:

        int r= TABLE.getSelectedRow();
          if(r!= -1){
          User_File fs=new User_File();
        ArrayList<User_Class> list=new ArrayList<>();
        list=fs.readfromFile();
        for(int i=0; i<list.size(); i++){
            if(r==i){
                list.remove(i);
                model.removeRow(r);
                JOptionPane.showMessageDialog(this, "Deleted succesfully");
            }
        } 
          
          fs.againWriteToFile();
          }
          else
              JOptionPane.showMessageDialog(this, "Please select any row and then press 'delete' button");
       

    }//GEN-LAST:event_delActionPerformed

