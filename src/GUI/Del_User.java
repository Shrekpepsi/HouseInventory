    private void ViewDataInTable(){
        
        ArrayList <User_Class> a =new ArrayList<>() ;
        a=F.readfromFile();
        
        for(User_Class u : a){
            Object[] o=new Object[4];
            
            o[0] = u.getName();
            o[1] = u.getEmail();
            o[2] = u.getCnic();
            o[3] = u.getPhone();
             
            model.addRow(o);
        }
    }
    
